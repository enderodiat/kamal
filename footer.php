				<h5>2. ¿QUÉ ES UN DATO DE CARÁCTER PERSONAL? ¿Y UN TRATAMIENTO?</h5>
				<p>Datos de carácter personal son aquellos datos que identifiquen o permitan identificar a una persona. Por ejemplo, el nombre y apellidos, el número de D.N.I., una imagen en la que se pueda reconocer a una persona, etc. Los datos de carácter personal incluyen información como el teléfono móvil, la dirección de correo electrónico y fecha de nacimiento, entre otros. Por otro lado, un tratamiento de carácter personal es toda operación o conjunto de operaciones que realizamos sobre tus datos personales, como, por ejemplo, la recogida, registro, conservación, utilización y comunicación de tus datos.</p>

				<h5>3. ¿QUÉ DATOS PERSONALES RECOPILAMOS Y A TRAVÉS DE QUÉ VÍAS LOS CAPTAMOS?</h5>
				<p>Para poder realizar los tratamientos de datos especificados en la presente Política de Privacidad, necesitamos determinada información sobre ti.  Por ello recopilamos la información que nos facilitas a través de la suscripción al sorteo. 
				Los datos que podemos recabar a través de la suscripción son tu nombre y apellidos, número de móvil, dirección de correo electrónico, información sobre tu uso y navegación por el Sitio Web (página web desde la que llegas al Sitio Web, capas del mismo visitadas, procesos iniciados y abandonados, dirección IP, tipo de dispositivo desde el que acceder, navegador de Internet empleado, tiempo que has permanecido en el Sitio Web, etc.), asimismo, almacenamos un “ID de sesión” correspondiente a un código interno que le identifica como usuario, opiniones sobre productos, servicios, etc.</p>

				<h5>4. ¿QUÉ DERECHOS PUEDES EJERCER?</h5>

				<p>A continuación, se especifican los derechos establecidos en el RGPD que puedes ejercitar, tal y como reconoce la Agencia Española de Protección de Datos.</p>

				<dl>
				<dt>A. Derecho de acceso</dt>
				<dd>Tienes derecho a que CENTRO HUARTE DE ARTE CONTEMPORÁNEO te confirme si está tratando o no tus datos personales, y en tal caso, derecho a conocer qué datos trata.</dd>

				<dt>B. Derecho de rectificación</dt>
				<dd>Consiste en la posibilidad de que pueda modificar aquellos datos que sean inexactos o incompletos, debiendo en la solicitud de rectificación indicar qué datos desea que se modifiquen.</dd>

				<dt>C. Derecho de oposición</dt>
				<dd>Puedes oponerte en cualquier momento a que tratemos sus datos. Tiene derecho a oponerse en cualquier momento, por motivos relacionados con su situación particular, a que datos personales que te conciernan sean objeto de un tratamiento basado en el interés legítimo de la empresa. CENTRO HUARTE DE ARTE CONTEMPORÁNEO dejará de tratar los datos personales, salvo que acreditemos motivos legítimos imperiosos para el tratamiento que prevalezcan sobre tus intereses, derechos y libertades, o para la formulación, el ejercicio o la defensa de reclamaciones.</dd>

				<dt>D. Derecho de supresión</dt>
				<dd>Este derecho permite la cancelación de tus datos personales. Esto no significa que tus datos sean totalmente eliminados, sino que tus datos se conservarán bloqueados de manera que impida su tratamiento, sin perjuicio de su puesta a disposición de las administraciones públicas, jueces y tribunales, para la atención de posibles responsabilidades que hayan surgido del tratamiento durante su plazo de prescripción. Una vez superado ese plazo, tus datos serán debidamente anonimizados o eliminados.</dd>

				<dt>E. Derecho a la portabilidad de tus datos</dt>

				<dd>Tiene derecho a copiar y transferir datos desde nuestra base de datos a otro CENTRO HUARTE DE ARTE CONTEMPORÁNEO del tratamiento. Solo es posible ejercer este derecho cuando el tratamiento esté basado en la ejecución de un contrato o en su consentimiento y el tratamiento se realice por medios automatizados.</dd>

				<dt>F. Derecho a la limitación en el tratamiento</dt>
				<dd>Este derecho te permite solicitarnos que suspendamos el tratamiento de tus datos cuando:</dd>
				<ul>
				<li>Hayas impugnado la exactitud de los datos, mientras CENTRO HUARTE DE ARTE CONTEMPORÁNEO verifica dicha exactitud.</li>
				<li>Hayas ejercido tu derecho de oposición al tratamiento de tus datos, mientras se verifica si los motivos legítimos de CENTRO HUARTE DE ARTE CONTEMPORÁNEO prevalecen sobre los tuyos como interesado.</li>
				</ul>

				<dd>Igualmente, este derecho te permite solicitar a CENTRO HUARTE DE ARTE CONTEMPORÁNEO que conserve tus datos personales cuando:</dd>
				<ul>
				<li>El tratamiento de datos sea ilícito y como interesado te opongas a la supresión de tus datos y solicites en su lugar una limitación de su uso.</li>
				<li>CENTRO HUARTE DE ARTE CONTEMPORÁNEO ya no necesite tus datos personales para los fines del tratamiento, pero los necesite para la formulación, ejercicio, o defensa de reclamaciones.</li>
				</ul>

				<dd>Podrás ejercer tus derechos, mandándonos tu petición a la siguiente dirección de correo electrónico administracion@centrohuarte.es   o bien por correo postal, acreditando en ambos casos tu identidad, dirigido a: Calle Calvario, 2, C.p. 31620 Huarte (Navarra). También puedes ejercitar tus derechos solicitando los impresos y formularios que a tal efecto tienes a tu disposición en todos nuestros establecimientos. Independientemente de la vía por la que optes, recuerda adjuntar una fotocopia de tu DNI o documento identificativo equivalente con el fin de que podamos confirmar tu identidad.</dd>

				<dd>Asimismo, te informamos de que, en el caso de que consideres que CENTRO HUARTE DE ARTE CONTEMPORÁNEO no ha satisfecho correctamente el ejercicio de tus derechos, podrás presentar una reclamación ante la Agencia Española de Protección de Datos (AEPD), domiciliada en calle Jorge Juan, nº 6, 28001, Madrid (Madrid). Si quieres conocer más información sobre cómo solicitar la protección de la AEPD, puedes dirigirte a su página web, http://www.agpd.es, o solicitar información en los número de teléfono 901 100 099 y 91.266.35.17.</dd>

