		<p>“Kamal” es un término árabe que significa equilibrio. Así se denominaba también a un
		antiguo instrumento cartográfico, que guiaba a los navegantes en latitudes ecuatoriales,
		tomando como referente la altura de la estrella polar.</p>

		<p>Como el “Kamal”, este proyecto de mapeo, trata de dar una orientación sobre el panorama
		artístico navarro y sus agentes culturales. De ofrecer una vista panorámica a un territorio
		vasto y diverso, siguiendo diversos criterios: geográficos, cronológicos, sociales, locales....
		Hemos hecho especial hincapié en el mundo de las escenas, a veces efímeras, y en todo caso,
		difíciles de documentar, que marcan este paisaje y los hilos que las entrelazan.</p>

		<p>Nos interesa abarcar eventos situados en un periodo amplio en el tiempo, tomando
		como eje inicial los Encuentros de 1972. Queremos reflexionar sobre cuestiones tales
		como: ¿qué bailarinas tienen una práctica política?, ¿qué relación existe entre la escena rock y
		la escena punk navarras?, ¿qué proyectos de comisariado originados en Navarra se
		desarrollan en el extranjero?, ¿qué cineastas tratan temas de género? o ¿cuántos espacios
		artísticos fueron clausurados durante la crisis económica del 2008 ?</p>

		<p>Además, hemos querido preservar la libertad de quien participe de autodefinirse. Por eso,
		“Kamal” trata de evitar el encasillamiento dentro de una disciplina artística, o dentro de una
		sola tendencia, permitiéndonos optar por definiciones polisémicas: azar, naturaleza, efímero,
		político, queer...</p>

		<p>Queremos crear un referente para la investigación que se auto-transforme en el tiempo, y en
		función de la intervención libre de quien participe, para documentar así la heterogeneidad y el
		cambio del tejido artístico contemporáneo navarro. Queremos rehuir las clasificaciones
		arbitrarias, los clichés y los estereotipos, y plantear de este modo una manera diferente de
		trazar mapas. “Kamal” será nuestro mapa, creado y transformado colectivamente.</p>

